const mongoose = require("mongoose");

const Schema = mongoose.Schema;

const lastChatSchema = new Schema(
  {
    userId: {
      type: String,
      required: true,
    },
    name: {
      type: String,
      required: true,
    },
    profileUrl: {
      type: String,
      required: true,
    },
    timesent: {
      type: String,
      required: true,
    },
    text: {
      type: String,
      required: true,
    },
    type: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

module.exports = lastChatSchema;
